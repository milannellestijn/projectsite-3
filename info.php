<P STYLE="font-weight: bold; font-size: 16px;"> Slaap goed</P>
<p STYLE="font-size: 16px;">
    Elk uur dat je minder slaapt dan de aanbevolen acht uur, kost je één punt van je IQ’, 
<br>dat beweert Stanley Conran, hoogleraar psychologie en slaapexpert aan de University of British Colombia, in zijn boek ‘Sleep Thieves’. 
<br>Ook wetenschappelijk onderzoeker Tom Rath schrijft in zijn boek ‘Eet, Beweeg, Slaap’ over het belang van een goede nachtrust. 
<br>Vier uur minder slaap staat volgens hem gelijk aan het drinken van een sixpack bier. 
<br>Om je brein fit te houden en je geheugen te trainen heb je dus voldoende slaap nodig.
<br>
<br>Slaap is nodig om herinneringen te evalueren en netjes in het brein te archiveren. 
<br>Een goede nachtrust zorgt ervoor dat informatie beter wordt gereproduceerd, 
<br>oude feiten makkelijker omhoog kunnen worden gehaald en nieuwe informatie beter kan worden opgeslagen.
<br>
<br>
<br>
</p>
<P STYLE="font-weight: bold; font-size: 16px;"> Goede voeding</P>
<p STYLE="font-size: 16px;">
    Goede voeding is minstens zo belangrijk voor het verbeteren van de cognitieve vaardigheden. 
<br>‘Het juiste dieet, lichaamsbeweging en slaap hebben de potentie om de gezondheid van onze hersenen en mentale functies positief te veranderen’, zegt Fernando Gómez-Pinilla, 
<br>hoogleraar neurochirurgie en fysiologische wetenschap aan UCLA. 
<br>Gómez-Pinilla deed jarenlang onderzoek naar de effecten van voeding, beweging en slaap op de hersenen. 
<br>‘Aanpassing van het dieet is een serieuze strategie voor het verbeteren van de cognitieve vaardigheden. 
<br>Het beschermt tegen hersenschade en gaat vergrijzing tegen.’
<br>
<br>Gómez-Pinilla analyseerde meer dan 160 studies over hoe een slim dieet het brein beïnvloedt. 
<br>Het juiste dieet bestaat volgens hem uit voedsel met een hoog gehalte Omega-3 vetzuren. 
<br>Zo hebben bijvoorbeeld zalm, walnoten en kiwi’s een gunstige invloed. 
<br>Omega-3 vetzuren zijn essentieel voor de normale hersenfuncties en lijken een positief effect te hebben op ons leervermogen en ons geheugen.
<br>
<br>
<br>
</p>
